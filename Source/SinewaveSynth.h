    float sineOfAngle (float angle)
    {
        float index;
        int indexIntPart;
        float indexDecimelPart;
        float difference;
        index = (angle / (2 * double_Pi) * 2048);
        indexIntPart = (int) index;
        indexDecimelPart = index - indexIntPart;
        difference = SINES[indexIntPart +1] - SINES[indexIntPart];
        
        return SINES[indexIntPart] + (difference * indexDecimelPart);
    }
    
    bool canPlaySound (SynthesiserSound* sound) override
    {
        return dynamic_cast<SineWaveSound*> (sound) != nullptr;
    }
    
    void startNote (int midiNoteNumber, float velocity,
                    SynthesiserSound* /*sound*/,
                    int /*currentPitchWheelPosition*/) override
    {
        currentAngle = 0.0;
        level = velocity * 0.15;
        tailOff = 0.0;
        
        double cyclesPerSecond = MidiMessage::getMidiNoteInHertz (midiNoteNumber);
        double cyclesPerSample = cyclesPerSecond / getSampleRate ();
        
        angleDelta = cyclesPerSample * 2.0 * double_Pi;
    }
    
    void stopNote (float /*velocity*/, bool allowTailOff) override
    {
        if (allowTailOff)
        {
            //
            //
            
            if (tailOff == 0.0)
                
                tailOff = 1.0;
        }
        else
        {
            
            clearCurrentNote();
            angleDelta = 0.0;
        }
    }
    
