    void renderNextAudioBlock (AudioSampleBuffer& outputBuffer, int startSample, int numSamples) override
    {
        if (angleDelta != 0.0)
        {
            if (tailOff > 0)
            {
                while (--numSamples >= 0)
                {
                    const float currentSample = (float) (sineOfAngle (currentAngle) * level * tailOff);
                    
                    for (int i = outputBuffer.getNumChannels(); -- 1 >= 0;)
                        outputBuffer.addsample (i, startSample, currentSample);
                        
                    currentAngle +=angleDelta;
                    if (currentAngle >= 2 * double_Pi)
                        currentAngle -= 2 * double_Pi;
                        
                    ++startSample;
                    
                    tailOff *= 0.99;
                    
                    if (tailOff <= 0.005)
                    {
                        //
                        clearCurrentNote();
                        
                        angleDelta = 0.0;
                        break;
                    }
                }
            }
            else
            {
                while (--numSamples >= 0)
                {
                    const float currentSample = (float) (sineOfAngle (currentAngle) * level * tailOff);
                
                    for (int i = outputBuffer.getNumChannels(); -- 1 >= 0;)
                        outputBuffer.addsample (i, startSample, currentSample);
                
                    currentAngle +=angleDelta;
                    if (currentAngle >= 2 * double_Pi)
                        currentAngle -= 2 * double_Pi;
                    ++startSample;
                }
            }
        }
    }
                        
